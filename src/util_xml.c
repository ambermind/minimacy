int codeFromEntity(char* entity, LINT len)
{
	int i;
	for (i = 0; i < XML_ENTITIES; i++)
	{
		if (!memcmp(entity, ((char**)xmlEntities)[i], len)) return xmlCodes[i];
	}
	return -1;
}